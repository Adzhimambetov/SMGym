        <h3 class="u-text u-text-1"> Как правильно составить программу тренировок?</h3>
        <p class="u-text u-text-2"> Программа тренировок - это индивидуально составленный план, координирующий тренировочный процесс, для достижения желаемого спортсменом результата. Грамотно построенная программа является основой качественного и безопасного тренинга вне зависимости от уровня подготовки атлета. В этой статье мы рассмотрим базовые моменты, на которые стоит обратить внимание при написании тренировочной программы.</p>
        <h2 class="u-text u-text-3"> Определение цели</h2>
        <p class="u-text u-text-4"> В первую очередь необходимо сформулировать цель тренировок и выделить основные направления работы. Причины для посещения тренажерного зала могут быть разными:<br>набор мышечной массы;<br>снижение веса или похудение;<br>развитие физических качеств (сила, ловкость, гибкость, координация, выносливость);<br>реабилитация после травм;<br>работа над рельефом мышц;<br>поддержание текущей физической формы
        </p>
        <h2 class="u-text u-text-5"> Сколько раз тренироваться в неделю?</h2>
        <p class="u-text u-text-6"> После выбора цели наступает этап формирования структуры программы тренировок. Количество тренировочных дней должно определяться исходя из вашего уровня подготовки. Начинающему спортсмену потребуется больше времени для восстановления, чем любителю или профессионалу. На этом уровне стоит начать с 2-3 дней в неделю, со временем постепенно увеличивая количество занятий. Также важно контролировать объем работы: если тренировка будет чрезмерно долгой и напряженной, то организму потребуется более продолжительный отдых. Оптимальное время занятий на первых этапах - 45-60 минут.</p>
        <h2 class="u-text u-text-7"> Какие мышечные группы тренировать и какие упражнения выбрать?</h2>
        <p class="u-text u-text-8"> Новичкам следует начинать именно с общих тренировок, где каждая мышечная группа получает сравнительно одинаковую нагрузку. Это необходимо для повышения работоспособности и укрепления организма в целом. К сплитам можно приступать уже более опытным спортсменам, для того чтобы лучше проработать те или иные мышцы и для сокращения времени тренировок.При построении общей тренировки важно начинать с упражнений на крупные мышечные группы: мышцы ног, спины и груди,- а затем переходить к более мелким: мышцам плечевого пояса, рук и живота.В сплите необходимо учитывать время восстановления мышц и то, что при тренировке одной мышечной группы в работу часто задействуются сопутствующие ей мышцы.Кроме разделения мышечных групп на крупные и мелкие их классифицируют как:<br>тянущие - ноги, спина, бицепс;<br>толкающие - дельты, грудь, трицепс.<br>В тренировочный день стоит делать акцент на 1 крупную мышечную группу, при чем ноги следует вынести в отдельную тренировку. Например, оптимальный сплит на 3 дня может выглядеть так:1 день - грудь+дельты<br>2 день - спина+руки<br>3 день - ноги.<br>Упражнения в вашей тренировочной программе должны соответствовать первоначальной цели. Например, для набора мышечной массы следует выполнять базовые упражнения, в которых участвуют в работе сразу несколько мышечных групп, начиная от сложных и заканчивая более простыми и изолирующими. Изолирующее упражнение - это упражнение, в котором нагрузка приходится на одну отдельную мышцу.Далее важно определить количество подходов и повторений. Классический вариант это 3-4 подхода в каждом упражнении.
        </p>
